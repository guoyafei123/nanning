	export default {
		data() {},
		mounted() {
			$(function() {
				var page = 1;
				var i = 3;
				var conheight = $('.min-cont').height();
				var lilen = $('img').length;
				var len = Math.ceil(lilen / i);
				// 向上
				$('.arrow-next').click(function() {
					if(page == len) {
						$('.min-map-list').animate({
							top: 0
						}, 'slow');
						page = 1;
					} else {
						$('.min-map-list').animate({
							top: '-=' + conheight
						}, 'slow');
						page++;
					}
				});
				// 向下
				$('.arrow-prev').click(function() {
					if(page == 1) {
						$('.min-map-list').animate({
							top: '-=' + conheight * (len - 1)
						}, 'slow');
						page = len;
					} else {
						$('.min-map-list').animate({
							top: '+=' + conheight
						}, 'slow');
						page--;
					}
				});
			})
		},
		methods: {

		},

	}
